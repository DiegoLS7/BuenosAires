    public void Listar() throws ProtocolException, IOException {
        URL direccion =  new URL("http://localhost:51214/api/Producto");
        HttpURLConnection con = (HttpURLConnection)direccion.openConnection();
        con.setRequestMethod("GET");
        
        int respuestaservidor = con.getResponseCode();
        if(respuestaservidor==200){
            BufferedReader in =  new BufferedReader(new InputStreamReader(con.getInputStream()));
            String datos;
            StringBuilder respuesta = new StringBuilder();
            while((datos = in.readLine())!=null){
                respuesta.append(datos);
            }
            in.close();
        try {
            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("id Productos");
            dtm.addColumn("Nombre");
            dtm.addColumn("Descripcion");
            dtm.addColumn("Stock");
            dtm.addColumn("Precio");
            dtm.addColumn("IMG");
            dtm.addColumn("ID Tipo");
            dtm.addColumn("ID Proveedor");
            
            JSONParser parser =  new JSONParser();
            Object productoJSON = parser.parse(respuesta.toString());
            JSONArray arreglo =  (JSONArray) productoJSON;
            String[] filas = new String[9];  
            for (int i = 0; i < arreglo.size(); i++) {
                JSONObject p = (JSONObject) arreglo.get(i);  
                filas[0] = p.get("Id_Producto")+" ";
                filas[1] =  p.get("Nombre")+" ";
                filas[2] =  p.get("Descripcion")+" ";
                filas[3] =  p.get("Stock")+" ";
                filas[4] =  p.get("precio")+" ";
                filas[5] =  p.get("Img")+" ";
                filas[6] =  p.get("Id_Tproducto")+" ";
                filas[7] =  p.get("Id_Proveedor")+" ";
                dtm.addRow(filas);
            }
            
            jTablaMant.setModel(dtm);
               
            

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en Tabla " + e.getMessage());
        }
    }
    else{
            System.out.println("Problemas con el servidor");
        }
        
    }
