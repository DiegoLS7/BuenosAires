    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // METODO MODIFICAR
        try {
            int idSolicitud = Integer.parseInt(SpIDSoli.getValue().toString());
            java.sql.Date fechaSolicitud;
            Date dateE = (Date) JdFech.getDate();
            if(dateE!=null){
                long dE = dateE.getTime();
                fechaSolicitud = new java.sql.Date(dE);
            }else{
                Date date = new Date();
                DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                dateFormat.format(date);
                JdFech.setDate(date);
                long dE = date.getTime();
                fechaSolicitud = new java.sql.Date(dE);
            }
            int ServicioID = Integer.parseInt(idServicioo.getValue().toString());
            int metodoID = Integer.parseInt(SpMetodo.getValue().toString());
            int usuarioID = Integer.parseInt(SpUsuID.getValue().toString());
            int confirmacion = Integer.parseInt(SpConfirmado.getValue().toString());
            
            SoliServ slse = new SoliServ(idSolicitud, fechaSolicitud, ServicioID, metodoID, usuarioID, confirmacion);

            if (soliDAO.modificarSoliServ(slse)) {
                JOptionPane.showMessageDialog(this, "Solicitud MODIFICADO");
                Listar();
            } else {
                JOptionPane.showMessageDialog(null, "Solicitud no se ha podido MODIFICAR");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR EN MODIFICAR" + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

