    public void buscarLista(int id) {
        try {

            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("id_Solicitud");
            dtm.addColumn("fecha");
            dtm.addColumn("Servicio ID");
            dtm.addColumn("METODO ID");
            dtm.addColumn("Usuario ID");
            dtm.addColumn("CONFIRMACION");
            
            List<SoliServ> lista = new SoliServicioDAO().ListarSoliServ();
            String[] filas  = new String[7]; 
            
            for (SoliServ soliserv : lista) {
                if(soliserv.getIdSolicitud()==(id)){
                filas[0] = String.valueOf(soliserv.getIdSolicitud());
                filas[1] = String.valueOf(soliserv.getFechaSolicitud());
                filas[2] = String.valueOf(soliserv.getServicioID());
                filas[3] = String.valueOf(soliserv.getMetodoID());
                filas[4] = String.valueOf(soliserv.getUsuarioID());
                filas[5] = String.valueOf(soliserv.getConfirmacion());

                    dtm.addRow(filas);
                }
            }
            JtablaSer.setModel(dtm);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en Tabla " + e.getMessage());
        }
    }

    
    public void Listar(){
    //METODO LISTAR
    try {
       
            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("id_Solicitud");
            dtm.addColumn("fecha");
            dtm.addColumn("Servicio ID");
            dtm.addColumn("METODO ID");
            dtm.addColumn("Usuario ID");
            dtm.addColumn("CONFIRMACION");
            
            List<SoliServ> lista = new SoliServicioDAO().ListarSoliServ();
            String[] filas  = new String[6];    
            
            for (SoliServ soliserv : lista) {
                filas[0] = String.valueOf(soliserv.getIdSolicitud());
                filas[1] = String.valueOf(soliserv.getFechaSolicitud());
                filas[2] = String.valueOf(soliserv.getServicioID());
                filas[3] = String.valueOf(soliserv.getMetodoID());
                filas[4] = String.valueOf(soliserv.getUsuarioID());
                filas[5] = String.valueOf(soliserv.getConfirmacion());
                
                dtm.addRow(filas);
            }
            JtablaSer.setModel(dtm);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en Tabla "+e.getMessage());
        }           
    
    }
