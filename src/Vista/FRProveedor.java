    public void buscarLista(int id) {
        try {

            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("id_proveedor");
            dtm.addColumn("nombre");
            dtm.addColumn("telefono");
            dtm.addColumn("email");

            List<Proveedor> lista = new ProveedorDAO().ListarProovedor();
            String[] filas = new String[5];

            for (Proveedor proveedor : lista) {
                if (proveedor.getIdProveedor()== (id)) {
                filas[0] = String.valueOf(proveedor.getIdProveedor());
                filas[1] = proveedor.getNombre();
                filas[2] = String.valueOf(proveedor.getTelefono());
                filas[3] = proveedor.getEmail();

                    dtm.addRow(filas);
                }
            }
            JTableProvee.setModel(dtm);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en Tabla " + e.getMessage());
        }
    }
