    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // METODO MODIFICAR
       try {
            int idProveedor = Integer.parseInt(SpIdProveedor.getValue().toString());
            String nombre = txtNombre.getText();
            int telefono = Integer.parseInt(SpTelefono.getValue().toString());
            String email = txtEmail.getText();   

            if(nombre.trim().length()<3){
                JOptionPane.showMessageDialog(this, "El Nombre debe tener al menos 3 caracteres");
                JOptionPane.showMessageDialog(null, "No se ha MODIFICADO");
                txtNombre.requestFocus();
                
            }else{
                Proveedor pro = new Proveedor(idProveedor, nombre, telefono, email);
                if (proDAO.modificarProveedor(pro)) {
                    JOptionPane.showMessageDialog(this, "Proveedor MODIFICADO");
                } else {
                    JOptionPane.showMessageDialog(null, "Proveedor no se ha podido MODIFICAR");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR EN MODIFICAR" + e.getMessage());
        }
        Listar();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            int idProveedor = Integer.parseInt(SpIdProveedor.getValue().toString());
            String nombre = txtNombre.getText();
            int telefono = Integer.parseInt(SpTelefono.getValue().toString());
            String email = txtEmail.getText();   

            if(nombre.trim().length()<3){
                JOptionPane.showMessageDialog(this, "El Nombre debe tener al menos 3 caracteres");
                JOptionPane.showMessageDialog(null, "No se ha registrado");
                txtNombre.requestFocus();
            }else if(email.trim().length()<3){
                JOptionPane.showMessageDialog(this, "El email debe tener al menos 3 caracteres");
                JOptionPane.showMessageDialog(null, "No se ha registrado");
                txtEmail.requestFocus();         
            } else{
                Proveedor pro = new Proveedor(idProveedor, nombre, telefono, email);
                if(proDAO.agregarProveedor(pro)){
                    JOptionPane.showMessageDialog(this, "Proveedor Registrado");
                    Listar();
                }else{
                    JOptionPane.showMessageDialog(null, "Proveedor no se ha podido registrar");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR EN REGISTRAR"+e.getMessage());
        }        
    }//GEN-LAST:event_jButton4ActionPerformed

