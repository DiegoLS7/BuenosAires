    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // METODO MODIFICAR
       try {
            int id_producto = Integer.parseInt(spIDProd.getValue().toString());
            String nombre = txtNombre.getText();
            String descripcion = txtDesc.getText();
            int stock = Integer.parseInt(SpStock.getValue().toString());
            int precio = Integer.parseInt(SpPrecio.getValue().toString());
            byte[] img = getImagen(Ruta);
            int tipo_producto_id_tproducto = Integer.parseInt(spTiP.getValue().toString());
            int proveedor_id_proveedor = Integer.parseInt(jSpinner4.getValue().toString());

            if (nombre.trim().length() < 3) {
                JOptionPane.showMessageDialog(this, "El Nombre debe tener al menos 3 caracteres");
                txtNombre.requestFocus();
            }else if (descripcion.trim().length() < 3) {
                JOptionPane.showMessageDialog(this, "LA descripcion debe tener al menos 3 caracteres");
                txtDesc.requestFocus();            
            }else{
                Productos pro = new Productos(id_producto, nombre, descripcion, stock, precio, img, tipo_producto_id_tproducto, proveedor_id_proveedor);
                if (proDAO.modificarProducto(pro)) {
                    JOptionPane.showMessageDialog(this, "Proveedor MODIFICADO");
                } else {
                    JOptionPane.showMessageDialog(null, "Proveedor no se ha podido MODIFICAR");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR EN MODIFICAR" + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

