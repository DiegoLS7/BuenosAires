    public void buscarLista(int id) {
        try {

            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("id Productos");
            dtm.addColumn("Nombre");
            dtm.addColumn("Descripcion");
            dtm.addColumn("Stock");
            dtm.addColumn("Precio");
            dtm.addColumn("IMG");
            dtm.addColumn("ID Tipo");
            dtm.addColumn("ID Proveedor");

            List<Productos> lista = new ProductoDAO().ListarProducto();
            String[] filas = new String[9];

            for (Productos productos : lista) {
                if (productos.getId_producto()== (id)) {
                filas[0] = String.valueOf(productos.getId_producto());
                filas[1] = productos.getNombre();
                filas[2] = productos.getDescripcion();
                filas[3] = String.valueOf(productos.getStock());
                filas[4] = String.valueOf(productos.getPrecio());
                filas[5] = String.valueOf(productos.getImg());
                filas[6] = String.valueOf(productos.getTipo_producto_id_tproducto());
                filas[7] = String.valueOf(productos.getProveedor_id_proveedor());

                    dtm.addRow(filas);
                }
            }
            jTablaMant.setModel(dtm);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en Tabla " + e.getMessage());
        }
    }
    private byte[] getImagen(String Ruta){
        File img = new File(Ruta);
        try {
            byte[] icono = new byte[(int) img.length()];
            InputStream input = new FileInputStream(img);
            input.read(icono);
            return icono;
        } catch (Exception e) {
            return null;
        }
    }
